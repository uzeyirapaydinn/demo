        [HttpPatch("update-refresh-token/{refreshTokensToken}")]
        [ProducesResponseType(StatusCodes.Status200OK, Type = typeof(int))]
        [ProducesResponseType(StatusCodes.Status400BadRequest, Type = typeof(string))]
        public async Task<IActionResult> RefreshTokensUpdateRefreshTokenAsync(string refreshTokensToken, [FromBody] RefreshTokensUpdateRefreshTokenRequestDto updateRequest)
        {
            var response = await mediator.Send(new RefreshTokensUpdateRefreshTokenCommand(refreshTokensToken, updateRequest));
            if (response.Code == 400)
            {
                return BadRequest($"Update Error: Response Code: {response.Code}, Message: {response.Message}");
            }
            else if (response.Code != 0)
            {
                return BadRequest($"Response Code: {response.Code}, Message: {response.Message}");
            }

            return Ok(response.Value);
        }
    }
